/*
Напишите программу, которая считывает возраст (u8) и два целых (u8) числа являющиеся показателями медицинского прибора для измерения артериального давления (АД):

    SYS - показывает систолическое (верхнее) давление.
    DIA - показывает диастолическое (нижнее) давление.

Программа должна вывести следующее:

    Если показатель(и) в норме, то напечатать:
    Систолическое АД в норме
    или
    Диастолическое АД в норме
    или
    Систолическое и Диастолическое АД в норме.
    В противном случае вывести на сколько завышен или занижен каждый показатель:
    Систолическое АД {} ниже нормы на {} или Систолическое АД {} выше нормы на {}
    Диастолическое АД {} ниже нормы на {} или Диастолическое АД {} выше нормы на {}.

Для наглядности посмотрите тестовые данные. Гарантируется, что возраст в заданном диапазоне.
Возрастные группы 	Систолическое АД 	Диастолическое АД
18-39 лет 	90-139 	60-89
40-59 лет 	91-149 	61-91
60+ лет 	91-159 	61-91
*/
use std::io::{self, Read};

fn main() {
    // Чтение входных данных
    let mut buf = String::new();
    io::stdin().read_to_string(&mut buf).unwrap();
    let mut nums = buf.split_whitespace().map(|s| s.parse::<u8>().unwrap());

    let age = nums.next().unwrap();
    let sys = nums.next().unwrap();
    let dia = nums.next().unwrap();

    // Определение нормальных диапазонов по возрасту
    let (sys_min, sys_max, dia_min, dia_max) = match age {
        18..=39 => (90, 139, 60, 89),
        40..=59 => (91, 149, 61, 91),
        _ => (91, 159, 61, 91), // 60+
    };

    let sys_ok = (sys_min..=sys_max).contains(&sys);
    let dia_ok = (dia_min..=dia_max).contains(&dia);

    // Формирование вывода
    if sys_ok && dia_ok {
        println!("Систолическое и Диастолическое АД в норме");
    } else {
        if !sys_ok {
            if sys < sys_min {
                println!("Систолическое АД {} ниже нормы на {}", sys, sys_min - sys);
            } else {
                println!("Систолическое АД {} выше нормы на {}", sys, sys - sys_max);
            }
        } else {
            println!("Систолическое АД в норме");
        }

        if !dia_ok {
            if dia < dia_min {
                println!("Диастолическое АД {} ниже нормы на {}", dia, dia_min - dia);
            } else {
                println!("Диастолическое АД {} выше нормы на {}", dia, dia - dia_max);
            }
        } else {
            println!("Диастолическое АД в норме");
        }
    }
}
