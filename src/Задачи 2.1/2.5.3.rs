/*
 Дан массив со строками, содержащими целые числа:
let arr: [&str; 3] = ["123", "456", "789"];

Преобразуйте этот массив так, чтобы значениями нового массива стали целые числа:
[i16, 3] [123, 456, 789];*/
fn main() {
    let txt: [&str; 3] = ["123", "456", "789"];
    let mut arr: [i16; 3] = [0; 3];
    for i in 0..txt.len() {
        arr[i] = txt[i].parse::<i16>().unwrap();
    }
    println!("{:?}", arr);
}
